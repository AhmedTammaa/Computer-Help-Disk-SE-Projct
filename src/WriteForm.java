    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        if(txt_issue_field.getText() == null || txt_issue_field.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(rootPane, "Empty Field, Please fill the issue!!!");
            return;
        }
        Form frm = new Form(txt_issue_title.getText(),txt_issue_field.getText(),false,user_ID);
        if(FormWriterController.Write(frm)){
        JOptionPane.showMessageDialog(rootPane, "Successfully sent");
        new UserHome().setVisible(true);
        this.setVisible(false);
        }else{
            JOptionPane.showConfirmDialog(null, "Form is not saved; please, try again!");
        }
    }//GEN-LAST:event_btnSendActionPerformed

